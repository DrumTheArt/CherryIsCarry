DROP TABLE IF EXISTS `TBL_PROJECT`;
CREATE TABLE IF NOT EXISTS `TBL_PROJECT` (
	`PK_id`	INTEGER NOT NULL,
	`TXT_title`	TEXT NOT NULL UNIQUE,
	`projectPrice`	REAL NOT NULL,
	`projectDeposite`	REAL NOT NULL,
	PRIMARY KEY(`PK_id`)
);
